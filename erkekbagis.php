<p><h4>2. EL EŞYA BAĞIŞ KAMPANYASI</h4></p>
<p>
Evlerini terk etmek zorunda kalan çoğunluğu çocuk ve kadınlardan oluşan Suriyeli kardeşlerimiz, Türkiye sınırındaki çadırkentlerde yaşam mücadelesi veriyor.
Onların kıyafet ihtiyaçlarını gidermek için kullanılmamış veya temiz kullanılmış 2. El kıyafet topluyoruz.</p>
<p>
Ayakkabı, halı, kilim, battaniye ve mutfak eşyası da bağışlayabilirsiniz.</p>
