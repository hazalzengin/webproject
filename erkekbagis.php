<p>
  ​GİYİM YARDIMLARI</p>
  <p>
Kızılay, ekonomik sıkıntılar sebebiyle giyim ihtiyacını karşılayamayan ihtiyaç sahiplerini sosyal incelemeler neticesinde belirlemekte ve sizlerin destekleri ile yüz binlerce ihtiyaç sahibini ayni ve nakdi şekilde yapılan yardımlarla sevindirmektedir.

İhtiyaç sahipleri, Kızılay Butik aracılığıyla tıpkı bir mağazadan alışveriş yapar gibi tanımlı parça adedi kadar kıyafeti ücretsiz bir şekilde alma imkânı bulurken; Sosyal Marketler aracılığıyla kendilerine tanımlanan limit doğrultusunda giyimden gıdaya, yakacaktan kırtasiyeye her türlü ihtiyaçlarını ücretsiz karşılayabilme imkânı bulmaktadırlar..

Siz de ihtiyaç sahiplerine giyim desteği sağlamak için Kızılay'a bağış yapabilir, Tüm GSM operatörleri üzerinden 2868'e “GIYIM” yazıp mesaj (SMS) yollayarak da 15₺ bağışta bulunabilirsiniz.
