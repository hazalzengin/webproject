<p><h4>
Neden Giyim Yardımı Yapıyoruz?</p></h4>
<p>
Soğuk kış günlerinde sırtında eski bir kazak olan mendil satan çocukları hepimiz görmüşüzdür. Yırtık ayakkabısının içine su dolmuş bir babayı ve elleri soğuktan çatlamış bir anayı. Bir Pazar köşesinde soğuktan titreyen ellerinde tuttuğu boş çantayı görmüşüzdür. Ve bu anlarda halimize binlerce kere şükrederiz öyle değil mi?
Asıl şükür “PAYLAŞMAKTIR" der büyüklerimiz. Bizi biz yapan giydiğimizden giydirmek, yediğimizden yedirmek değil midir?
</p>
<p>
Hepimiz çocukken ailemizin aldığı bayramlığa çok sevinmişizdir. Hiç ihtiyacımız olmadığı bir anda, bir sevdiğimizden aldığımız bir hediye bizi ne kadar da mutlu etmiştir değil mi?
Hediyeleşmek güzeldir, hem vereni hem alanı mutlu eder.</p>
<p>
Bir bayram sabahı kimsesizin mutluluğuna şahit ol ve üşüyen minik ayaklarda bir bot, sırtında bir mont ol. Hiç tanımadığın ve belki de tanıyamayacağın bir yetimin yüzünde tebessüm ol, yoksul bir babanın bükülmüş sırtında yeni bir elbise ol, Paylaş ve Mutlu ol.
</p>
