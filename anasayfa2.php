<h2 style="color:black">Kullanılmayan Giysi Bağışı</h2>
<img  src="https://torbaliguncel.com/images/haberler/2018/05/torbali-yardim-1.jpg"width="950" height="550">
<p>
  Unutmayın, kullanılmayan bekleyen giysi birilerini sevindirir. Artık kullanmadığınız eşyalarınız, dolap bekleyen giysileriniz işe yarasın, birilerini memnun etsin istemez misiniz?

Özellikle soğuk kış aylarında ihtiyaç sahiplerine bağışlamak istediğiniz eşyalarınız varsa, İlkem merkezi \"Atmayın, bağışlayın\" sosyal sorumluluk projesiyle sizden alıp ihtiyaç sahiplerine ulaşıyor.

Vereceğiniz eşyaları mutlaka kolileyin. Ya da doğrudan siz teslim edecekseniz uygun bir ambalaja koymaya özen gösterin.

İlkem merkez ofisi, Cuma-Cumartesi 12.00-20.00 saatleri arasında bağışlarınızı kabul etmek için açık olacaktır.

Unutmayın kullanılmayan giysiler birilerini sevindirir.

Atmayın, bağışlayın.</p>
